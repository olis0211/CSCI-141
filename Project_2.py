def make_phone_number(area_codes = AREA_CODES, sep = '-'): #do not change
    if area_codes== AREA_CODES or  area_codes == list:
        area_codes = random.choice(area_codes)
    else:
        area_codes = area_codes
    number = str(area_codes)+str(sep)+str(make_prefix())+str(sep)+str(make_suffix())
    return (number)

def make_prefix(): #do not change
    digit1= random.choice('0123456789') #do not change or delete
    digit2 = random.choice('0123456789') #do not change or delete
    if(digit1== '5' and digit2 == '5'):
        digit3= random.choice('0123456789')
    elif(digit1 == '9' and digit2 == '5'):
        digit3 = random.choice('023456789')
    else:
        digit3 = random.choice('1234567890')
    prefix = digit1 + digit2 + digit3
    return (prefix)

def make_suffix(): #do not change
    suffix = ' '
    while len(suffix) < 4:
        suffix = str(suffix) + random.choice('0123456789')
    return (suffix)


